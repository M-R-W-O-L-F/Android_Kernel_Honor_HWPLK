    pinctrl_cmds_tx(pdev, liszt_lcd_pinctrl_lowpower_cmds,ARRAY_SIZE(liszt_lcd_pinctrl_lowpower_cmds));
    mdelay(200);

    checksum_enable_ctl = false;

