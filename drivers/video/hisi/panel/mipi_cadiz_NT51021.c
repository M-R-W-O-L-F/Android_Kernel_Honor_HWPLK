static int mipi_liszt_panel_set_backlight(struct platform_device *pdev)
{
    struct hisi_fb_data_type *hisifd = NULL;
    u32 level = 0;
    u32 old_level = 0;
    u8 sony_ipc_bl_level = 0;
    int ret = 0;
    int bias_gpio_status = 0;
    //app_min_bl
    const int min_bl = 4;
    //driver_bl
    const int dimming_valve = 30;
    //app_bl - drive_bl = bl_differ;
    const int bl_differ = 4;

    char led_page0[2] = {0x83, 0x00};       /* DTYPE_GEN_WRITE2 */
    char led_page1[2] = {0x84, 0x00};       /* DTYPE_GEN_WRITE2 */
    char bl_level_adjust[2] = {0x9f, 0x00}; /* DTYPE_GEN_WRITE2 */
    struct dsi_cmd_desc bl_cmd[] = {
        {DTYPE_GEN_WRITE2, 0, 100, WAIT_TYPE_US, sizeof(led_page0), led_page0},
        {DTYPE_GEN_WRITE2, 0, 100, WAIT_TYPE_US, sizeof(led_page1), led_page1},
        {DTYPE_GEN_WRITE2, 0, 16, WAIT_TYPE_MS, sizeof(bl_level_adjust), bl_level_adjust},
    };

    char set_still_dimming[2] = {0x95,0x00};
    char set_moving_dimming[2] = {0x94, 0x00};
    struct dsi_cmd_desc set_dimming_cmd[] = {
        {DTYPE_GEN_WRITE2, 0,10, WAIT_TYPE_US, sizeof(select_page2_step0), select_page2_step0},
        {DTYPE_GEN_WRITE2, 0,10, WAIT_TYPE_US, sizeof(select_page2_step1), select_page2_step1},
        {DTYPE_GEN_WRITE2, 0,10, WAIT_TYPE_US, sizeof(set_still_dimming), set_still_dimming},
        {DTYPE_GEN_WRITE2, 0,10, WAIT_TYPE_US, sizeof(set_moving_dimming), set_moving_dimming},
    };

    BUG_ON(pdev == NULL);
    hisifd = platform_get_drvdata(pdev);
    BUG_ON(hisifd == NULL);

    HISI_FB_DEBUG("fb%d, +.\n", hisifd->index);

    level = hisifd->bl_level;
    old_level = hisifd->backlight.bl_level_old;
    bl_level_adjust[1] = ( level > (min_bl-1)) ? level : min_bl;
    //app_bl - drive_bl = bl_differ;
    bl_level_adjust[1] = (( bl_level_adjust[1] > (255-bl_differ)) ? 255 : (bl_level_adjust[1]+bl_differ));
    old_level = ((old_level < (255-bl_differ)) ? (old_level+bl_differ) : 255);

    if(hisifd->backlight.bl_level_old != hisifd->bl_level ){
#ifndef SONY_IPC_BYPASS
    if(lcd_pwm_type == LCD_USE_IPC_PWM){
        sony_ipc_bl_level = (u8)(hisifd->bl_level / 2);
        cadiz_i2c_write_u16(0x0C28,sony_ipc_bl_level);
        bias_gpio_status = gpio_get_value(gpio_lcd_bias_enable);
        if (!bias_gpio_status){
            gpio_cmds_tx(liszt_lcd_enable_bl_cmds, ARRAY_SIZE(liszt_lcd_enable_bl_cmds));
        }
        HISI_FB_INFO("fb%d, -.\n", hisifd->index);
        return ret;
    }
#endif

        if (timer_restart){
            hrtimer_cancel(&cabc_dimming_hrtimer);
            timer_restart = 0;
        }
        HISI_FB_INFO("driver set bl = %d , app set bl = %d!\n", bl_level_adjust[1], hisifd->bl_level);
        bias_gpio_status = gpio_get_value(gpio_lcd_bias_enable);
        if (!bias_gpio_status){
            set_still_dimming[1] = (bl_level_adjust[1] > dimming_valve) ? STILL_DIMMING_LONG : STILL_DIMMING_SHORT;
            set_moving_dimming[1] = (bl_level_adjust[1] > dimming_valve) ? MOVING_DIMMING_LONG : MOVING_DIMMING_SHORT;
            mipi_dsi_cmds_tx(set_dimming_cmd, ARRAY_SIZE(set_dimming_cmd), hisifd->mipi_dsi0_base);
            mipi_dsi_cmds_tx(bl_cmd, ARRAY_SIZE(bl_cmd), hisifd->mipi_dsi0_base);
            gpio_cmds_tx(liszt_lcd_enable_bl_cmds, ARRAY_SIZE(liszt_lcd_enable_bl_cmds));
        }else{
            if (old_level > dimming_valve){
            set_still_dimming[1] = STILL_DIMMING_LONG;
            set_moving_dimming[1] = MOVING_DIMMING_LONG;
            }
            else{
                set_still_dimming[1] = (bl_level_adjust[1] > dimming_valve) ? STILL_DIMMING_LONG : STILL_DIMMING_SHORT;
                set_moving_dimming[1] = (bl_level_adjust[1] > dimming_valve) ? MOVING_DIMMING_LONG : MOVING_DIMMING_SHORT;
            }
            mipi_dsi_cmds_tx(set_dimming_cmd, ARRAY_SIZE(set_dimming_cmd), hisifd->mipi_dsi0_base);
            mipi_dsi_cmds_tx(bl_cmd, ARRAY_SIZE(bl_cmd), hisifd->mipi_dsi0_base);

            if (old_level > dimming_valve && bl_level_adjust[1] < (dimming_valve+1) && hisifd->bl_level > 0){
                timer_restart = 1;
                hrtimer_init(&cabc_dimming_hrtimer, CLOCK_MONOTONIC, HRTIMER_MODE_REL);
                cabc_dimming_hrtimer.function = hrtimer_set_dimming;
                hrtimer_start(&cabc_dimming_hrtimer, ktime_set(ESD_CHECK_TIME_PERIOD / 1000,\
                (ESD_CHECK_TIME_PERIOD % 1000) * 1000000), HRTIMER_MODE_REL);
            }
        }
    }

    HISI_FB_INFO("fb%d, -.\n", hisifd->index);

    return ret;
}


