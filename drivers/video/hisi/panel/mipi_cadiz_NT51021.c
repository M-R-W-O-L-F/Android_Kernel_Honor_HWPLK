static ssize_t mipi_liszt_pic_enhance_mode_store(struct platform_device *pdev,
    const char *buf, size_t count)
{
    int ret = 0;
    int length = 0;
    int i = 0;
    unsigned long value = 0xFF;

    struct hisi_fb_data_type *hisifd = NULL;

    BUG_ON(pdev == NULL);
    hisifd = platform_get_drvdata(pdev);
    BUG_ON(hisifd == NULL);

    HISI_FB_ERR("fb%d, +.\n", hisifd->index);

    if(atomic_read(&set_ipc_enhance_mode)){
        HISI_FB_ERR("atomic_read failed.\n");
        return count;
    }

    atomic_set(&set_ipc_enhance_mode, 1);

    value = simple_strtoul(buf,NULL,10);
    if (value >= 5){
        HISI_FB_ERR("%s: input value is error.\n",__func__);
        goto out;
    }

    HISI_FB_INFO("input value =  %ld\n", value);
#if 0
    if(g_pic_enhance_mode == value){
        goto out;
    }
#endif
    /*load effect arrays*/
    ret = sony_cadiz_change_pic_state(value);
    if (ret >= 0)
    {
        g_pic_enhance_mode = value;
        HISI_FB_ERR("g_pic_enhance_mode value is %d\n",  g_pic_enhance_mode);
    }
    else
    {
        HISI_FB_ERR("switch state failed, original state is %d\n", g_pic_enhance_mode);
    }

    /* open or close blocks*/
    if (value == LCD_PIC_ENHANCE_STATE_DEFAULT)
    {
        HISI_FB_ERR("pic state default\n");
        /* GRC/MNR+SPC+ICE On (COM off)*/
        sony_candiz_blocks_select(0);
    }
    else if (value == LCD_PIC_ENHANCE_STATE_GAME)
    {
        HISI_FB_ERR("pic state game\n");
        /*GRC/MNR +COM off (SPC + iCE On)*/
        sony_candiz_blocks_select(2);
    }
    else if (value == LCD_PIC_ENHANCE_STATE_TEXT)
    {
        HISI_FB_ERR("pic state text\n");
        /*GRC/MNR+COM+ICE off (SPC On)*/
        sony_candiz_blocks_select(3);
    }
    else if (value == LCD_PIC_ENHANCE_STATE_CLOSED)
    {
        HISI_FB_ERR("pic state closed\n");
        /*All Off*/
        sony_candiz_blocks_select(5);
    }
    else
    {
        HISI_FB_ERR("pic state error.\n");
        goto out;
    }

out:
    atomic_set(&set_ipc_enhance_mode, 0);
    HISI_FB_ERR("fb%d, -.\n", hisifd->index);
    return count;
}


